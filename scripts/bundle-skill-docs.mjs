#!/usr/bin/env node
// Pack-time bundler for Claude-plugin skill docs. Zero dependencies — run with
// the repo's own Node.
//
// The convention (the same source-first rule as the workspace's editable
// installs): in the repo, skill markdown carries ordinary RELATIVE LINKS into
// `docs/guide/` — no build step, never stale, `tsx`-era dev just works. Only
// when the plugin package is PACKED (`pnpm pack` / `pnpm publish`, via its
// prepack hook) does this script bundle: every relative link that escapes the
// package is resolved, its target copied into a `references/` folder beside
// the linking file (markdown copies get a GENERATED banner), and the link
// rewritten to point at the copy — so the shipped skill is self-contained.
// postpack restores the tree exactly; nothing generated is ever committed.
//
// Subcommands (cwd = the plugin package dir for pack/restore — they run as
// that package's prepack/postpack):
//   pack      bundle + rewrite in place, saving originals to .skill-bundle-backup/
//   restore   undo the rewrite: restore the original markdown files
//   --check   (from anywhere) verify every relative link in plugin markdown
//             resolves to a real file, and no half-restored state is lying around
//
// `restore` deliberately does NOT delete the generated references/ folders:
// pnpm prints its pack report (stat-ing the packed files) AFTER postpack, so
// deleting them there fails the whole pack with ENOENT. They are gitignored,
// inert in dev (dev markdown never links to them), and wiped + regenerated by
// the next `pack`. A crashed pack can leave the markdown rewritten; `pack`
// restores any leftover backup before starting and `restore` is a no-op
// without one, so the pair is idempotent. `--check` in CI catches broken
// links and a leftover backup (= rewritten markdown in the tree).

import {
  cpSync,
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { basename, dirname, join, relative, resolve, sep } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const PLUGIN_PKG_DIR = join(repoRoot, "packages", "aiui-claude-plugin");
const BACKUP_DIR = ".skill-bundle-backup";

/** Where the published docs site serves a guide page (the banner's pointer). */
const SITE_BASE = "https://habemus-papadum.github.io/pdum_aiui/guide/";

/** Every .md file under a directory, recursively (skipping generated folders). */
function markdownFiles(dir) {
  const found = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === "references" || entry.name === "node_modules") {
        continue;
      }
      found.push(...markdownFiles(path));
    } else if (entry.name.endsWith(".md")) {
      found.push(path);
    }
  }
  return found;
}

/**
 * Markdown inline links: `[text](target)`, capturing the target. Deliberately
 * simple — the plugin's own markdown is ours to keep parseable (no angle
 * brackets, no titles, no reference-style links pointing at files).
 */
const LINK_RE = /\]\(([^)\s]+)\)/g;

/** A link this script manages: relative, into the filesystem, not an anchor. */
function isRelativeFileLink(target) {
  return (
    !/^[a-z][a-z0-9+.-]*:/i.test(target) && // http:, https:, mailto:, vscode:, …
    !target.startsWith("/") &&
    !target.startsWith("#")
  );
}

/** Split a link target into its path and `#fragment`/`?query` suffix. */
function splitTarget(target) {
  const cut = target.search(/[#?]/);
  return cut === -1 ? [target, ""] : [target.slice(0, cut), target.slice(cut)];
}

/**
 * Scan one markdown file for relative links that escape `packageDir`,
 * resolving each against the file's own directory. Returns
 * `{ target, path, suffix }` per escaping link (path absolute), and the list
 * of broken relative links (resolving nowhere) for --check.
 */
function escapingLinks(mdPath, packageDir) {
  const text = readFileSync(mdPath, "utf8");
  const escaping = [];
  const broken = [];
  for (const match of text.matchAll(LINK_RE)) {
    const target = match[1];
    if (!isRelativeFileLink(target)) {
      continue;
    }
    const [pathPart, suffix] = splitTarget(target);
    if (pathPart === "") {
      continue; // pure-fragment forms like `](#section)` never reach here, but be safe
    }
    const resolved = resolve(dirname(mdPath), pathPart);
    if (!existsSync(resolved)) {
      broken.push(target);
      continue;
    }
    if (!(resolved + sep).startsWith(packageDir + sep)) {
      escaping.push({ target, path: resolved, suffix });
    }
  }
  return { text, escaping, broken };
}

/** The GENERATED banner prepended to bundled markdown copies. */
function banner(sourceAbs) {
  const sourceRel = relative(repoRoot, sourceAbs);
  const page = sourceRel.startsWith(join("docs", "guide") + sep)
    ? `${SITE_BASE}${basename(sourceRel, ".md")}`
    : "https://github.com/habemus-papadum/pdum_aiui";
  return (
    `<!-- GENERATED at pack time — bundled copy of ${sourceRel} (the source of truth),\n` +
    `     published at ${page}. Relative links inside this copy resolve in the source\n` +
    `     repo, not necessarily here. -->\n\n`
  );
}

// ── pack / restore ────────────────────────────────────────────────────────────

/** Delete every generated references/ dir under the plugin's marketplace. */
function removeReferenceDirs(dir) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (!entry.isDirectory()) {
      continue;
    }
    const path = join(dir, entry.name);
    if (entry.name === "references") {
      rmSync(path, { recursive: true, force: true });
    } else if (entry.name !== "node_modules") {
      removeReferenceDirs(path);
    }
  }
}

function packPlugin(packageDir) {
  restorePlugin(packageDir, { quiet: true }); // un-rewrite any crashed-pack leftovers
  removeReferenceDirs(join(packageDir, "marketplace")); // stale bundles from the last pack
  const backupDir = join(packageDir, BACKUP_DIR);
  const state = { rewritten: [], created: [] };
  // copy path → source path, so two different sources colliding on one
  // basename in one references/ dir refuse instead of silently aliasing.
  const copySource = new Map();

  for (const mdPath of markdownFiles(join(packageDir, "marketplace"))) {
    const { text, escaping, broken } = escapingLinks(mdPath, packageDir);
    if (broken.length > 0) {
      throw new Error(
        `${relative(repoRoot, mdPath)}: broken relative link(s): ${broken.join(", ")}`,
      );
    }
    if (escaping.length === 0) {
      continue;
    }
    const referencesDir = join(dirname(mdPath), "references");
    let rewritten = text;
    for (const { target, path, suffix } of escaping) {
      const name = basename(path);
      const copyPath = join(referencesDir, name);
      const priorSource = copySource.get(copyPath);
      if (priorSource === undefined) {
        copySource.set(copyPath, path);
        mkdirSync(referencesDir, { recursive: true });
        if (name.endsWith(".md")) {
          writeFileSync(copyPath, banner(path) + readFileSync(path, "utf8"));
        } else {
          cpSync(path, copyPath);
        }
        state.created.push(relative(packageDir, copyPath));
      } else if (priorSource !== path) {
        throw new Error(
          `${relative(repoRoot, referencesDir)}/${name}: two different sources collide ` +
            `(${relative(repoRoot, priorSource)} vs ${relative(repoRoot, path)})`,
        );
      }
      rewritten = rewritten.replaceAll(`](${target})`, `](references/${name}${suffix})`);
    }
    const mdRel = relative(packageDir, mdPath);
    const backupPath = join(backupDir, mdRel);
    mkdirSync(dirname(backupPath), { recursive: true });
    writeFileSync(backupPath, text);
    writeFileSync(mdPath, rewritten);
    state.rewritten.push(mdRel);
  }

  mkdirSync(backupDir, { recursive: true });
  writeFileSync(join(backupDir, "state.json"), JSON.stringify(state, null, 2));
  console.log(
    `[skill-bundle] bundled ${state.created.length} file(s), rewrote ${state.rewritten.length} markdown file(s)`,
  );
}

function restorePlugin(packageDir, { quiet = false } = {}) {
  const backupDir = join(packageDir, BACKUP_DIR);
  const statePath = join(backupDir, "state.json");
  if (!existsSync(statePath)) {
    return;
  }
  const state = JSON.parse(readFileSync(statePath, "utf8"));
  for (const mdRel of state.rewritten) {
    cpSync(join(backupDir, mdRel), join(packageDir, mdRel));
  }
  // The generated references/ folders stay on disk (gitignored): pnpm stats
  // the packed files for its report AFTER postpack, so deleting them here
  // turns every pack into an ENOENT failure. The next pack wipes them.
  rmSync(backupDir, { recursive: true, force: true });
  if (!quiet) {
    console.log(
      `[skill-bundle] restored ${state.rewritten.length} markdown file(s) ` +
        `(${state.created.length} gitignored bundle file(s) stay until the next pack)`,
    );
  }
}

// ── check (CI) ────────────────────────────────────────────────────────────────

function check() {
  const problems = [];
  if (existsSync(join(PLUGIN_PKG_DIR, BACKUP_DIR))) {
    problems.push(
      `${BACKUP_DIR}/ exists — a pack crashed before restoring; ` +
        "run the restore subcommand in the plugin package",
    );
  }
  for (const mdPath of markdownFiles(join(PLUGIN_PKG_DIR, "marketplace"))) {
    const rel = relative(repoRoot, mdPath);
    const { broken } = escapingLinks(mdPath, PLUGIN_PKG_DIR);
    for (const target of broken) {
      problems.push(`${rel}: relative link does not resolve: ${target}`);
    }
  }
  if (problems.length > 0) {
    console.error("skill markdown links are unhealthy:");
    for (const problem of problems) {
      console.error(`  ${problem}`);
    }
    process.exit(1);
  }
  console.log("skill markdown links all resolve; no pack leftovers");
}

const mode = process.argv[2];
if (mode === "--check") {
  check();
} else if (mode === "pack") {
  packPlugin(process.cwd());
} else if (mode === "restore") {
  restorePlugin(process.cwd());
} else {
  console.error(
    `unknown argument ${JSON.stringify(mode)} — usage: bundle-skill-docs.mjs pack|restore|--check`,
  );
  process.exit(1);
}
